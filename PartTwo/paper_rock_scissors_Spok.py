"""Камень, ножницы, бумага, ящерица, Спок"""

timur = input()
ruslan = input()

var_dict = {'камень':['бумага', 'Спок'], 'ножницы': ['камень', 'Спок'],
            'бумага': ['ножницы','ящерица'], 'Спок': ['ящерица','бумага'],
            'ящерица': ['ножницы','камень'],
           }

if ruslan in var_dict[timur]:
    print('Руслан')
elif ruslan == timur:
    print('ничья')
else:
    print('Тимур')
